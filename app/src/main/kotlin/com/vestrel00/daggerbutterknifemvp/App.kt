package com.vestrel00.daggerbutterknifemvp

import android.app.Activity
import android.app.Application
import dagger.android.DispatchingAndroidInjector
import dagger.android.HasActivityInjector
import javax.inject.Inject

/**
 * The Android [Application].
 *
 * **DEPENDENCY INJECTION**
 *
 * We could extend [dagger.android.DaggerApplication] so we can get the boilerplate dagger code for
 * free. However, we want to avoid inheritance (if possible and it is in this case) so that we have
 * to option to inherit from something else later on if needed
 */
class App : Application(), HasActivityInjector {

    @Inject
    lateinit var activityInjector: DispatchingAndroidInjector<Activity>

    override fun onCreate() {
        super.onCreate()
        DaggerAppComponent.builder().create(this).inject(this)
    }

    override fun activityInjector() = activityInjector
}